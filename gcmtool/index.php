<div class=content>

<p>The GCMTools are a suite of tools for autiting and editing GameCube 
DVD images (.iso and .gcm). This will allow you to modify content in games, 
browse the game structure, and even aide in the development of homebrew 
games and programs.</p>

<p>GCMTools should compile in linux, bsd, OSX, Darwin, and others for 
x86 and PPC (and possibly other architectures). The new 0.3.0 
package has been fully tested on OSX 10.3 and Linux on an x86 and a 
PPC. Please report any bugs if you run across any.</p>

<p>Nearly all of the code in this is based on information from the 
documentation at <a href="http://www.gc-linux.org">gc-linux.org</a>.</p>

<p>For you advanced types, I've got code in the CVS (get the gcmtool 
package, the testbed one is outdated; I was playing with cvs my first 
day.)</p>

<p>Version 0.3.0 contains a lot of new tools for working with various 
parts of a GCM including the disk header, and <a href="bnrs.gif">bnr 
files</a>.</>

<p>Also in the works is a GUI-based app for OSX... [ <a 
href="/gcmbrowser/">Screenshots</a> ] Since it's based on some old 
code, I'll release a binary and sourcecode when I fix it up again.</p>

<h2>So, what can you do with gcmtool?</h2>

<p>Currently, it displays various info about a game (region, game name, 
display format [ntsc or pal], and some info about offsets). You can also 
extract the disk header, disk header info, apploader, and main 
executable DOL, as well as replace the disk header and disk header info. 
You can even extract files and view the full file list.</p>

